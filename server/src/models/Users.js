import express from 'express'
import mongoose from 'mongoose'

const UserSchema = new mongoose.Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true,
        unique: true
    }

})

export const UserModel = mongoose.model('users', UserSchema)